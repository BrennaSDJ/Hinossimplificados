            <p><span class="titulo">Hymn 29 – A Poor Wayfaring Man of Grief Lyrics:</span></p>

                    <div class="verse">
                        <p> A poor wayfaring Man of grief Hath often crossed me on my way,<br>

                            Who sued so humbly for relief That I could never answer nay.<br>
                            
                            I had not pow’r to ask his name, Whereto he went, or whence he came;<br>
                            
                            Yet there was something in his eye That won my love; I knew not why.</p>
                    </div>

                    <div class="verse">
                        <p>  Once, when my scanty meal was spread, He entered; not a word he spake,<br>

                            Just perishing for want of bread. I gave him all; he blessed it, brake,<br>
                            
                            And ate, but gave me part again. Mine was an angel’s portion then,<br>
                            
                            For while I fed with eager haste, The crust was manna to my taste.</p>
                    </div>

                    <div class="verse">
                        <p>   I spied him where a fountain burst Clear from the rock; his strength was gone.<br>

                            The heedless water mocked his thirst; He heard it, saw it hurrying on.<br>
                            
                            I ran and raised the suff’rer up; Thrice from the stream he drained my cup,<br>
                            
                            Dipped and returned it running o’er; I drank and never thirsted more.</p>
                    </div>

                    <div class="verse">
                        <p> Twas night; the floods were out; it blew A winter hurricane aloof.<br>

                            I heard his voice abroad and flew To bid him welcome to my roof.<br>
                            
                            I warmed and clothed and cheered my guest And laid him on my couch to rest,<br>
                            
                            Then made the earth my bed and seemed In Eden’s garden while I dreamed.</p>
                    </div>

                    <div class="verse">
                        
                        <p> Stript, wounded, beaten nigh to death, I found him by the highway side.<br>

                            I roused his pulse, brought back his breath, Revived his spirit, and supplied<br>
                            
                            Wine, oil, refreshment—he was healed. I had myself a wound concealed,<br>
                            
                            But from that hour forgot the smart, And peace bound up my broken heart.</p>
                            
                    </div>

                    <div class="verse">
                        
                        <p> In pris’n I saw him next, condemned To meet a traitor’s doom at morn.<br>

                            The tide of lying tongues I stemmed, And honored him ’mid shame and scorn.<br>
                            
                            My friendship’s utmost zeal to try, He asked if I for him would die.<br>
                            
                            The flesh was weak; my blood ran chill, But my free spirit cried, “I will!”</p>
                                                                
                    </div>

                    <div class="verse">
                        
                        <p>. 7. Then in a moment to my view The stranger started from disguise.<br>

                            The tokens in his hands I knew; The Savior stood before mine eyes.<br>
                            
                            He spake, and my poor name he named, “Of me thou hast not been ashamed.<br>
                            
                            These deeds shall thy memorial be; Fear not, thou didst them unto me.”</p>
                                                                
                    </div>

